package com.apms.mobile;

public class WarningInfoForCfd {

	private String acnum;
	private String flightno;
	private String reporttime;
	private String atano;
	private String warn_content;

	public String getAcnum() {
		return acnum;
	}

	public void setAcnum(String acnum) {
		this.acnum = acnum;
	}

	public String getFlightno() {
		return flightno;
	}

	public void setFlightno(String flightno) {
		this.flightno = flightno;
	}

	public String getReporttime() {
		return reporttime;
	}

	public void setReporttime(String reporttime) {
		this.reporttime = reporttime;
	}

	public String getAtano() {
		return atano;
	}

	public void setAtano(String atano) {
		this.atano = atano;
	}

	public void setWarn_content(String warn_content) {
		this.warn_content = warn_content;
	}

	public String getWarn_content() {
		return warn_content;
	}
}
