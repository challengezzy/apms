package com.apms.bs.dataprase.vo;

import com.apms.vo.ApmsVarConst;

public class AcarsParseResult {
	
	private int status;//解析结果
	
	private String reason;//原因
	
	public AcarsParseResult(){
		status = ApmsVarConst.TELEGRAPH_PARSE_OK; //默认解析OK
	}
	
	public AcarsParseResult(int status,String reason){
		this.status = status;
		this.reason = reason;
	}
	
	public AcarsParseResult(int status){
		this.status = status;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}
	
	

}
