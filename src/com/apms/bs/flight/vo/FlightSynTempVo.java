package com.apms.bs.flight.vo;

/**
 * 航班数据同步临时表对象
 * @author jerry
 * @date Mar 21, 2014
 */
public class FlightSynTempVo {
	
	private String flt_pk;
	
	private String hashvalue;
	
	private String flttype_dep; //起飞机场航班维护种类
	
	private String flttype_arr;
	
	private String nextFltPk; //该飞机下一个航班PK
	private String nextFltNo; //该飞机下一个航班PK
	
	private String preFltPk; //该飞机上一个航班PK

	public String getFlt_pk() {
		return flt_pk;
	}

	public void setFlt_pk(String flt_pk) {
		this.flt_pk = flt_pk;
	}

	public String getHashvalue() {
		return hashvalue;
	}

	public void setHashvalue(String hashvalue) {
		this.hashvalue = hashvalue;
	}

	public String getFlttype_dep() {
		return flttype_dep;
	}

	public void setFlttype_dep(String flttype_dep) {
		this.flttype_dep = flttype_dep;
	}

	public String getFlttype_arr() {
		return flttype_arr;
	}

	public void setFlttype_arr(String flttype_arr) {
		this.flttype_arr = flttype_arr;
	}

	public String getNextFltPk() {
		return nextFltPk;
	}

	public void setNextFltPk(String nextFltPk) {
		this.nextFltPk = nextFltPk;
	}

	public String getNextFltNo() {
		return nextFltNo;
	}

	public void setNextFltNo(String nextFltNo) {
		this.nextFltNo = nextFltNo;
	}

	public String getPreFltPk() {
		return preFltPk;
	}

	public void setPreFltPk(String preFltPk) {
		this.preFltPk = preFltPk;
	}
	
	

}
