package com.apms.matlab.vo;

public class MathOneXFitResult {
	
	//一元线性回归
	private int times; //拟合的多项式次数
	
	private double[] coefficients; //多项式系统 ,从最高到最低次数 [a,b,c,d...n]

	public int getTimes() {
		return times;
	}

	public void setTimes(int times) {
		this.times = times;
	}

	public double[] getCoefficient() {
		return coefficients;
	}

	public void setCoefficients(double[] coefficients) {
		this.coefficients = coefficients;
	}
	
	

}
