package com.apms.matlab.vo;

import com.mathworks.toolbox.javabuilder.webfigures.WebFigure;

/**
 * 调用Matlab傅里叶变换计算结果
 * @author jerry
 * @date Jun 18, 2013
 */
public class FftCountResult {
	
	/**
	 * 是否显示图表数据
	 */
	private boolean showPic;
	
	private WebFigure tFigure;//时域图形
	
	private WebFigure fFigure;//频域图形
	
	private double[] timeX = new double[0]; //时域图X轴数据
	
	private double[] freqX = new double[0] ;//频域图X轴数据
	
	private double[] timeY = new double[0]; //时域图Y轴数据
	
	private double[] freqY = new double[0] ;//频域图Y轴数据

	public boolean isShowPic() {
		return showPic;
	}

	public void setShowPic(boolean showPic) {
		this.showPic = showPic;
	}

	public WebFigure gettFigure() {
		return tFigure;
	}

	public void settFigure(WebFigure tFigure) {
		this.tFigure = tFigure;
	}

	public WebFigure getfFigure() {
		return fFigure;
	}

	public void setfFigure(WebFigure fFigure) {
		this.fFigure = fFigure;
	}

	public double[] getTimeX() {
		return timeX;
	}

	public void setTimeX(double[] timeX) {
		this.timeX = timeX;
	}

	public double[] getFreqX() {
		return freqX;
	}

	public void setFreqX(double[] freqX) {
		this.freqX = freqX;
	}

	public double[] getTimeY() {
		return timeY;
	}

	public void setTimeY(double[] timeY) {
		this.timeY = timeY;
	}

	public double[] getFreqY() {
		return freqY;
	}

	public void setFreqY(double[] freqY) {
		this.freqY = freqY;
	}
	
}
